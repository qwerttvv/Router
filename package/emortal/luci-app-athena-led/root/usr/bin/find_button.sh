#!/bin/sh

echo "📸 [1/3] 正在记录松开按键时的默认引脚状态..."
cat /sys/kernel/debug/gpio > /tmp/g1

echo ""
echo "👉 第一步完成！"
echo "🔴 现在，请【死死按住】路由器上的物理按键（千万别松手！）"
echo "⌨️ 保持按住的同时，在键盘上敲一下回车键..."
read -r

echo "📸 [2/3] 正在记录按住按键时的状态..."
cat /sys/kernel/debug/gpio > /tmp/g2

echo ""
echo "✅ 好了，可以松开按键了！"
echo "🔍 [3/3] 正在对比底层硬件引脚变化..."
echo "-----------------------------------"

awk 'NR==FNR{a[$1]=$0;next} a[$1]!=$0 {print "🕵️ 发现嫌疑人引脚！\n[松开时]: " a[$1] "\n[按住时]: " $0 "\n"}' /tmp/g1 /tmp/g2

echo "-----------------------------------"
echo "🎉 测试结束！如果找到了类似 gpio-71 的引脚，请前往 Web 界面填入数字即可。"